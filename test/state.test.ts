describe('Markdown plan discovery', () => {
  it('discovers root markdown plan files before asking for a new idea', async () => {
    const cwd = await mkdtemp(path.join(os.tmpdir(), 'gaud-plan-discovery-'));
    try {
      await writeFile(path.join(cwd, 'README.md'), '# Docs\n', 'utf8');
      await writeFile(path.join(cwd, 'google-drive-firmware-plan.md'), '# Google Drive Firmware Plan\n', 'utf8');

      await expect(discoverDefaultPlanPath(cwd)).resolves.toBe('google-drive-firmware-plan.md');
    } finally {
      await rm(cwd, { recursive: true, force: true });
    }
  });

  it('prefers PLAN.md over generated .gaud plans when both exist', async () => {
    const cwd = await mkdtemp(path.join(os.tmpdir(), 'gaud-plan-priority-'));
    try {
      await mkdir(path.join(cwd, '.gaud', 'plans'), { recursive: true });
      await writeFile(path.join(cwd, '.gaud', 'plans', 'gaud-20260604T213949-plan.md'), '# Previous Plan\n', 'utf8');
      await writeFile(path.join(cwd, 'PLAN.md'), '# Primary Plan\n', 'utf8');

      await expect(discoverDefaultPlanPath(cwd)).resolves.toBe('PLAN.md');
    } finally {
      await rm(cwd, { recursive: true, force: true });
    }
  });

  it('falls back to the newest previous .gaud plan when no root plan exists', async () => {
    const cwd = await mkdtemp(path.join(os.tmpdir(), 'gaud-plan-history-'));
    try {
      const planDir = path.join(cwd, '.gaud', 'plans');
      const olderPath = path.join(planDir, 'gaud-20260604T213048-plan.md');
      const newerPath = path.join(planDir, 'gaud-20260604T213949-plan.md');
      await mkdir(planDir, { recursive: true });
      await writeFile(olderPath, '# Older Plan\n', 'utf8');
      await writeFile(newerPath, '# Newer Plan\n', 'utf8');
      await utimes(olderPath, new Date('2026-06-04T21:30:48Z'), new Date('2026-06-04T21:30:48Z'));
      await utimes(newerPath, new Date('2026-06-04T21:39:49Z'), new Date('2026-06-04T21:39:49Z'));

      await expect(discoverDefaultPlanPath(cwd)).resolves.toBe(path.join('.gaud', 'plans', 'gaud-20260604T213949-plan.md'));
    } finally {
      await rm(cwd, { recursive: true, force: true });
    }
  });

  it('does not fall back to another discovered plan when an explicit markdown path is missing', async () => {
    const cwd = await mkdtemp(path.join(os.tmpdir(), 'gaud-plan-missing-explicit-'));
    try {
      await writeFile(path.join(cwd, 'PLAN.md'), '# Existing Plan\n', 'utf8');

      const source = await resolvePlanningSource(cwd, 'missing-plan.md');

      expect(source.missingExplicitPath).toBe(true);
      expect(source.sourcePath).toBe('missing-plan.md');
      expect(source.absoluteSourcePath).toBe(path.join(cwd, 'missing-plan.md'));
    } finally {
      await rm(cwd, { recursive: true, force: true });
    }
  });
});

describe('Active Run Discovery', () => {
  it('should find the latest active run from the .gaud/runs directory by createdAt', async () => {
    const cwd = await mkdtemp(path.join(os.tmpdir(), 'gaud-active-run-'));
    try {
      const runsDir = path.join(cwd, '.gaud', 'runs');
      await mkdir(runsDir, { recursive: true });

      // Lexically newer, but terminal. Must be ignored.
      const doneDir = path.join(runsDir, 'gaud-20260608T140000');
      await mkdir(doneDir, { recursive: true });
      await writeFile(
        path.join(doneDir, 'state.json'),
        JSON.stringify({
          id: 'gaud-20260608T140000',
          status: 'done',
          task: 'done task',
          createdAt: 3,
          updatedAt: 3,
          repoRoot: cwd,
          runDir: doneDir,
          eventsPath: path.join(doneDir, 'events.jsonl'),
          statePath: path.join(doneDir, 'state.json'),
          tmuxSocket: 'gaud-20260608T140000',
          tmuxSession: 'gaud-20260608T140000',
          workers: {},
          lastEventOffset: 0,
        }),
        'utf8'
      );

      // Lexically older, but most recently created active run. Must win.
      const runningDir = path.join(runsDir, 'gaud-20260608T120000');
      await mkdir(runningDir, { recursive: true });
      await writeFile(
        path.join(runningDir, 'state.json'),
        JSON.stringify({
          id: 'gaud-20260608T120000',
          status: 'running',
          task: 'running task',
          createdAt: 2,
          updatedAt: 2,
          repoRoot: cwd,
          runDir: runningDir,
          eventsPath: path.join(runningDir, 'events.jsonl'),
          statePath: path.join(runningDir, 'state.json'),
          tmuxSocket: 'gaud-20260608T120000',
          tmuxSession: 'gaud-20260608T120000',
          workers: {},
          lastEventOffset: 0,
        }),
        'utf8'
      );

      const olderActiveDir = path.join(runsDir, 'gaud-20260608T130000');
      await mkdir(olderActiveDir, { recursive: true });
      await writeFile(
        path.join(olderActiveDir, 'state.json'),
        JSON.stringify({
          id: 'gaud-20260608T130000',
          status: 'starting',
          task: 'older active task',
          createdAt: 1,
          updatedAt: 1,
          repoRoot: cwd,
          runDir: olderActiveDir,
          eventsPath: path.join(olderActiveDir, 'events.jsonl'),
          statePath: path.join(olderActiveDir, 'state.json'),
          tmuxSocket: 'gaud-20260608T130000',
          tmuxSession: 'gaud-20260608T130000',
          workers: {},
          lastEventOffset: 0,
        }),
        'utf8'
      );

      const active = await findLatestActiveRun(cwd);
      expect(active).toBeDefined();
      expect(active?.id).toBe('gaud-20260608T120000');
      expect(active?.status).toBe('running');
    } finally {
      await rm(cwd, { recursive: true, force: true });
    }
  });

  it('should return undefined if there are no active runs', async () => {
    const cwd = await mkdtemp(path.join(os.tmpdir(), 'gaud-no-active-run-'));
    try {
      const runsDir = path.join(cwd, '.gaud', 'runs');
      await mkdir(runsDir, { recursive: true });

      const run1Dir = path.join(runsDir, 'gaud-20260608T120000');
      await mkdir(run1Dir, { recursive: true });
      await writeFile(
        path.join(run1Dir, 'state.json'),
        JSON.stringify({
          id: 'gaud-20260608T120000',
          status: 'done',
          task: 'task 1',
          createdAt: Date.now(),
          updatedAt: Date.now(),
          repoRoot: cwd,
          runDir: run1Dir,
          eventsPath: path.join(run1Dir, 'events.jsonl'),
          statePath: path.join(run1Dir, 'state.json'),
          tmuxSocket: 'gaud-20260608T120000',
          tmuxSession: 'gaud-20260608T120000',
          workers: {},
          lastEventOffset: 0,
        }),
        'utf8'
      );

      const active = await findLatestActiveRun(cwd);
      expect(active).toBeUndefined();
    } finally {
      await rm(cwd, { recursive: true, force: true });
    }
  });
});

describe('extractPlanPath', () => {
  it('should extract the plan path from task description', () => {
    const task = `Some description\n\nExecution plan: /path/to/my-plan.md`;
    expect(extractPlanPath(task)).toBe('/path/to/my-plan.md');
  });

  it('should ignore status/trailing output details and select the active/existing plan path', () => {
    const task = `M1 — Plan, implement, verify: some task\nExecution plan: /non-existent-plan.md ✓ worker done\n\nExecution plan: /path/to/my-plan.md`;
    expect(extractPlanPath(task)).toBe('/path/to/my-plan.md');
  });

  it('should prioritize the existing plan path over non-existing ones', async () => {
    const tempDir = await mkdtemp(path.join(os.tmpdir(), 'gaud-test-'));
    try {
      const existingPlan = path.join(tempDir, 'existing-plan.md');
      await writeFile(existingPlan, 'plan content');
      
      const task = `M1 — Plan\nExecution plan: /non-existent-path.md ✓ worker done\n\nExecution plan: ${existingPlan}`;
      expect(extractPlanPath(task)).toBe(existingPlan);
    } finally {
      await rm(tempDir, { recursive: true, force: true });
    }
  });
});

