describe('State Serialization', () => {
  it('should serialize and deserialize a GaudRunState object correctly', () => {
    const run: GaudRunState = {
      id: 'gaud-123',
      status: 'running',
      task: 'implement feature',
      createdAt: Date.now(),
      updatedAt: Date.now(),
      repoRoot: '/root',
      runDir: '/root/.gaud/runs/gaud-123',
      eventsPath: '/root/.gaud/runs/gaud-123/events.jsonl',
      statePath: '/root/.gaud/runs/gaud-123/state.json',
      tmuxSocket: 'gaud-123',
      tmuxSession: 'gaud-123',
      workers: {},
      lastEventOffset: 0,
    };

    const json = JSON.stringify(run);
    const deserialized = JSON.parse(json) as GaudRunState;

    expect(deserialized.id).toBe(run.id);
    expect(deserialized.status).toBe(run.status);
    expect(deserialized.task).toBe(run.task);
  });
});

describe('Polling Bridge - Event Parsing', () => {
  it('should parse valid callback events from a JSONL string', () => {
    const eventsJsonl = [
      JSON.stringify({ ts: Date.now(), runId: 'gaud-123', workerId: 'frontend', type: 'done', summary: 'Implemented UI' }),
      JSON.stringify({ ts: Date.now(), runId: 'gaud-123', workerId: 'backend', type: 'waiting-user', question: 'Confirm API' }),
    ].join('\n');

    const lines = eventsJsonl.split('\n');
    const parsedEvents = lines.map(line => JSON.parse(line));

    expect(parsedEvents).toHaveLength(2);
    expect(parsedEvents[0].workerId).toBe('frontend');
    expect(parsedEvents[0].type).toBe('done');
    expect(parsedEvents[1].workerId).toBe('backend');
    expect(parsedEvents[1].type).toBe('waiting-user');
  });
});

describe('Agent command generation', () => {
  it('prefixes every real agent invocation with B2V disabled', () => {
    for (const agent of ['claude', 'codex', 'gemini', 'opencode', 'agy', 'custom-agent']) {
      const command = agentCommand(agent, agent, '/tmp/prompt.txt', false);
      expect(command.startsWith("bash -lc 'B2V_DISABLED=true ")).toBe(true);
      expect(command).toContain(agent);
    }
  });

  it('uses Codex persistent YOLO mode instead of one-shot exec', () => {
    const command = agentCommand('codex', 'codex', '/tmp/prompt.txt', false);

    expect(command).toContain('codex');
    expect(command).toContain('--yolo');
    expect(command).not.toContain('codex exec');
    expect(command).not.toContain('< <(');
  });

  it('uses Gemini YOLO interactive mode', () => {
    const command = agentCommand('gemini', 'gemini', '/tmp/prompt.txt', false);

    expect(command).toContain('gemini');
    expect(command).toContain('--yolo');
    expect(command).toContain(' -i ');
  });

  it('uses OpenCode prompt mode and an automatic callback fallback', () => {
    const command = agentCommand('opencode', 'opencode', '/tmp/prompt.txt', false);

    expect(command).toContain('opencode');
    expect(command).toContain('--prompt');
    expect(command).not.toContain(' run ');
    expect(command).toContain('completed without explicit callback');
  });

  it('uses Antigravity print mode instead of unsupported run subcommand', () => {
    const command = agentCommand('agy', 'agy', '/tmp/prompt.txt', false);

    expect(command).toContain('agy');
    expect(command).toContain('--print');
    expect(command).toContain('--dangerously-skip-permissions');
    expect(command).not.toContain(' run ');
  });

  it('reconstructs restart worker env with B2V disabled and callback metadata', () => {
    const envPrefix = workerEnvPrefix({ id: 'worker one', agent: 'opencode', role: 'Implementer', workstream: 'ui polish' });

    expect(envPrefix.startsWith('B2V_DISABLED=true ')).toBe(true);
    expect(envPrefix).toContain("GAUD_WORKER_ID='worker one'");
    expect(envPrefix).toContain("GAUD_WORKER_ROLE='Implementer'");
    expect(envPrefix).toContain("GAUD_WORKSTREAM='ui polish'");
    expect(envPrefix).toContain("GAUD_AGENT='opencode'");
  });
});

describe('ask_user dialog rendering', () => {
  const ansiTheme = {
    fg: (_color: string, text: string) => `\x1b[38;2;1;2;3m${text}\x1b[39m`,
  };

  it('keeps every rendered line within the terminal width', () => {
    const width = 40;
    const lines = renderAskUserDialogLines({
      question: 'Release script is ready to run, but it uploads GitHub release assets and the channel changes updater metadata.',
      options: [
        {
          label: 'Nightly draft',
          description: 'Recommended. Run `pnpm release -- --channel nightly` (pre-release, updates latest-nightly.json). Safer because it does not move stable users.',
        },
        {
          label: 'Stable draft',
          description: 'Run `pnpm release -- --channel stable` (production updater latest.json). Use this only if the release is intended for all stable users now.',
        },
        {
          label: 'Other — type my own answer',
          description: 'Custom answer',
        },
      ],
      optionIndex: 0,
      editMode: false,
      width,
      theme: ansiTheme,
    });

    expect(lines.length).toBeGreaterThan(0);
    for (const line of lines) {
      expect(visibleWidth(line)).toBeLessThanOrEqual(width);
    }
  });

  it('also truncates embedded editor lines', () => {
    const width = 32;
    const lines = renderAskUserDialogLines({
      question: 'Custom response?',
      options: [{ label: 'Other — type my own answer', description: 'Custom answer' }],
      optionIndex: 0,
      editMode: true,
      editorLines: ['this custom answer is much longer than the dialog can display safely'],
      width,
      theme: ansiTheme,
    });

    for (const line of lines) {
      expect(visibleWidth(line)).toBeLessThanOrEqual(width);
    }
  });
});

